def add_strings(path: Path, strings: dict, tips: list | None = None):
    content = path.read_text(encoding="utf-8")
    adds = []
    for k, v in strings.items():
        if f'name="{k}"' in content:
            continue
        val = v.replace("&", "&amp;").replace("<", "&lt;")
        adds.append(f'    <string name="{k}">{val}</string>')
    if tips and 'name="about_usage_tips"' not in content:
        items = "\n".join(f'        <item>{t.replace("&", "&amp;").replace("<", "&lt;")}</item>' for t in tips)
        adds.append(f'    <string-array name="about_usage_tips">\n{items}\n    </string-array>')
    if adds:
        content = content.replace("</resources>", "\n".join(adds) + "\n</resources>")
        path.write_text(content, encoding="utf-8")
        print(f"Added {len(adds)} entries to {path.name}")


def apply_replacements(path: Path, reps: list[tuple[str, str]]) -> int:
    text = path.read_text(encoding="utf-8")
    count = 0
    for old, new in reps:
        if old in text:
            text = text.replace(old, new)
            count += 1
        else:
            print(f"  MISSING in {path.name}: {old[:60]}...")
    path.write_text(text, encoding="utf-8")
    return count


